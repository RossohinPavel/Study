""" Упражнение 1
Напишите программу, которая принимает целое число xx и определяет, принадлежит ли данное число указанному промежутку. 
От -1 до 17.
Формат входных данных
На вход программе подаётся целое число x.
Формат выходных данных
Программа должна вывести текст в соответствии с условием задачи.
Примечание. Если точка выколотая, то граница не включается, если точка закрашенная, то граница включается.
Sample Input 1: 2
Sample Output 1: Принадлежит
Sample Input 2: -790
Sample Output 2: Не принадлежит
Sample Input 3: -1
Sample Output 3: Не принадлежит
"""
print('Принадлежит' if -1 < int(input()) < 17 else 'Не принадлежит')


""" Упражнение 2
Напишите программу, которая принимает целое число xx и определяет, принадлежит ли данное число указанным промежуткам.
Меньше -3 или больше 7 включительно
Формат входных данных
На вход программе подаётся целое число xx.
Формат выходных данных
Программа должна вывести текст в соответствии с условием задачи.
Примечание. Если точка выколотая, то граница не включается, если точка закрашенная, то граница включается. 
Sample Input 1: -44
Sample Output 1: Принадлежит
Sample Input 2: -3
Sample Output 2: Принадлежит
Sample Input 3: 5
Sample Output 3: Не принадлежит
"""

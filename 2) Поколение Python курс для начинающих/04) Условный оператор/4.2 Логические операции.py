""" Упражнение 4
Назовем число красивым, если оно является четырехзначным и делится нацело на 7 или на 17. Напишите программу, 
определяющую, является ли введённое число красивым. Программа должна вывести «YES», если число является красивым, 
или «NO» в противном случае.
Формат входных данных
На вход программе подаётся натуральное число.
Формат выходных данных
Программа должна вывести текст в соответствии с условием задачи.
Sample Input 1: 1043
Sample Output 1: YES
Sample Input 2: 1045
Sample Output 2: NO
Sample Input 3: 2751
Sample Output 3:YES
"""
a = int(input())
print('YES' if len(str(a)) == 4 and (a % 7 == 0 or a % 17 == 0) else 'NO')


""" Упражнение 5
Напишите программу, которая принимает три положительных числа и определяет, существует ли невырожденный треугольник с 
такими сторонами.
Формат входных данных
На вход программе подаётся три положительных целых числа.
Формат выходных данных
Программа должна вывести «YES» или «NO» в соответствии с условием задачи.
Примечание. Треугольник существует, если выполняется неравенство треугольника.
Sample Input 1:
    5
    2
    3
Sample Output 1: NO
Sample Input 2:
    3
    4
    6
Sample Output 2: YES
Sample Input 3:
    8
    2
    4
Sample Output 3: NO
"""
a, b, c = map(lambda x: int(input()), range(3))
print('YES' if a < b+c and b < a+c and c < a+b else 'NO')


""" Упражнение 6
Напишите программу, которая определяет, является ли год с данным номером високосным. Если год является високосным, то 
выведите «YES», иначе выведите «NO».
Год является високосным, если его номер кратен 4, но не кратен 100, или если он кратен 400.
Формат входных данных
На вход программе подаётся натуральное число.
Формат выходных данных
Программа должна вывести текст в соответствии с условием задачи.
Sample Input 1: 2020
Sample Output 1: YES
Sample Input 2: 2012
Sample Output 2: YES
Sample Input 3: 2009
Sample Output 3: NO
"""

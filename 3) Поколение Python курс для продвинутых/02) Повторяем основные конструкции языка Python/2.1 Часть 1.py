n, k = list(range(1, 1 + int(input()))), int(input())
c = 0
s = len(n) - 1
flag = True
while True:
    if flag:
        for i in range(len(n)):
            if n[i] != 0:
                c += 1
            if c == k:
                n[i] = 0
                c = 0
                s -= 1
            if s == 0:
                flag = False
                break
    else:
        break
print(sum(n))    

# –í–∞—Ä–∏–∞–Ω—Ç 1
def chunked(lst, num):
    chunk = []
    temp = []
    c = 0
    for i in lst.split():
        temp.append(i)
        c += 1
        if c == num:
            chunk.append(temp)
            temp = []
            c = 0
    if temp:
        chunk.append(temp)
    return chunk
print(chunked(input(), int(input())))



import random


def generate_password(length):
    pw = []
    chair = 'abcdefghijkmnpqrstuvwxyzABCDEFGHJKLMNPQRSTUVWXYZ23456789'
    while True:
        check = [False, False, False]
        pw = random.sample(chair, length)
        for i in pw:
            if check[0] is False and i.islower():
                check[0] = True
            if check[1] is False and i.isupper():
                check[1] = True
            if check[2] is False and i.isdigit():
                check[2] = True
        if all(check):
            break
    return ''.join(pw)


def generate_passwords(count, length):
    pws = []
    for i in range(count):
        pws.append(generate_password(length))
    return pws



from functools import reduce
def evaluate(coefficients, x):
    x = int(x)
    cof = coefficients.split()
    coefficients = map(lambda q, st: int(q)*(x**st), cof, range(len(cof)-1, -1, -1))
    return reduce(lambda a, b: a + b, coefficients)

print(evaluate(input(), input()))

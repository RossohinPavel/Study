""" Упражнение 5
Анаграммы — это слова, которые состоят из одинаковых букв. Например:
    адаптер — петарда
    адресочек — середочка
    азбука — базука
    аистенок — осетинка
Реализуйте функцию group_anagrams(), которая принимает один аргумент:
    words — список слов
Функция должна группировать в кортежи слова из списка words, являющиеся анаграммами, и возвращать список полученных 
кортежей.
Примечание 1. Порядок кортежей в возвращаемом функцией списке, а также порядок элементов в этих кортежах, не важен.
Примечание 2. В тестирующую систему сдайте программу, содержащую только необходимую функцию group_anagrams(), 
но не код, вызывающий ее.
Примечание 3. Тестовые данные доступны по ссылке. 
Sample Input 1:
    groups = group_anagrams(['evil', 'father', 'live', 'levi', 'book', 'afther', 'boko'])
    
    print(*groups)
Sample Output 1:
    ('boko', 'book') ('evil', 'levi', 'live') ('afther', 'father')
Sample Input 2:
    groups = group_anagrams(['evil', 'father', 'book', 'stepik', 'beegeek'])
    
    print(*groups)
Sample Output 2:
    ('beegeek',) ('book',) ('evil',) ('father',) ('stepik',)
Sample Input 3:
    groups = group_anagrams(['крона', 'сеточка', 'тесачок', 'лучик', 'стоечка', 'норка', 'чулки'])
    
    print(*groups)
Sample Output 3:
    ('крона', 'норка') ('сеточка', 'тесачок', 'стоечка') ('лучик', 'чулки')
"""
import itertools as it

def group_anagrams(words):
    srt = lambda x: sorted(x)
    dct = it.groupby(sorted(words, key=srt), key=srt)
    for i, v in dct:
        yield tuple(v)


""" Упражнение 6
Будем считать, что последовательность целых неотрицательных чисел можно преобразовать в отрезок, если разница между 
соседними элементами этой последовательности равна единице. Например, числа 3, 4, 5, 6, 7,8 можно 
преобразовать в отрезок [3;8][3;8]. Числа 1, 3, 5, 11, 15, 22 в отрезок преобразовать нельзя. Одиночное 
число преобразуется в отрезок, в котором и правой, и левой границей является оно само. Например, число 1 можно 
преобразовать в отрезок [1;1][1;1].
Реализуйте функцию ranges(), которая принимает один аргумент:
numbers — список различных натуральных чисел, расположенных в порядке возрастания
Функция должна преобразовывать числа из списка numbers в отрезки, представляя их в виде кортежей, где первый элемент 
кортежа является левой границей отрезка, второй элемент — правой границей отрезка. Полученные кортежи-отрезки функция 
должна возвращать в виде списка.
Примечание 1. Числа в возвращаемом функцией списке должны располагаться в своем исходном порядке.
Примечание 2. В тестирующую систему сдайте программу, содержащую только необходимую функцию ranges(), но не код, 
вызывающий ее.
Примечание 3. Тестовые данные доступны по ссылке. 
Sample Input 1:
    numbers = [1, 2, 3, 4, 7, 8, 10]
    
    print(ranges(numbers))
Sample Output 1:
    [(1, 4), (7, 8), (10, 10)]
Sample Input 2:
    numbers = [1, 3, 5, 7]
    
    print(ranges(numbers))
Sample Output 2:
    [(1, 1), (3, 3), (5, 5), (7, 7)]
Sample Input 3:
    numbers = [1, 2, 3, 4, 5, 6, 7]
    
    print(ranges(numbers))
Sample Output 3:
    [(1, 7)]
"""
def ranges(numbers):
    i = 0
    lst = []
    while i < len(numbers):
        cur_lst = [numbers[i], numbers[i]]
        while i+1 != len(numbers) and numbers[i]+1 == numbers[i + 1]:
            i += 1
            cur_lst[1] = numbers[i]
        lst.append(tuple(cur_lst))
        i += 1
    return lst
